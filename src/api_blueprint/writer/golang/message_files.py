from __future__ import annotations

from collections.abc import Mapping, Sequence
from dataclasses import dataclass
from fnmatch import fnmatch
from pathlib import Path
import re
from typing import Any


GENERATED_MARKER = "// Code generated by api-blueprint"
CASE_VARIANT_CHUNK_SIZE = 50

_FIRST_CAP_RE = re.compile(r"(.)([A-Z][a-z]+)")
_ALL_CAP_RE = re.compile(r"([a-z0-9])([A-Z])")
_NON_IDENTIFIER_RE = re.compile(r"[^A-Za-z0-9]+")

_STALE_MESSAGE_FILE_PATTERNS = (
    "gen_messages.go",
    "gen_message_cases.go",
    "gen_*_message.go",
    "gen_*_constructors.go",
    "gen_*_processor.go",
    "gen_*_visitor.go",
    "gen_*_cases.go",
    "gen_*_cases_*.go",
)


@dataclass(frozen=True)
class GoMessageFile:
    filename: str
    template_name: str
    context: dict[str, Any]


def go_message_file_slug(name: str) -> str:
    spaced = _FIRST_CAP_RE.sub(r"\1_\2", name)
    spaced = _ALL_CAP_RE.sub(r"\1_\2", spaced)
    slug = _NON_IDENTIFIER_RE.sub("_", spaced).strip("_").lower()
    return slug or "message"


def plan_go_message_files(
    message_unions: Sequence[Mapping[str, Any]],
    message_cases: Sequence[Mapping[str, Any]],
) -> tuple[GoMessageFile, ...]:
    files: list[GoMessageFile] = []
    for union in message_unions:
        slug = go_message_file_slug(str(union["name"]))
        files.append(
            GoMessageFile(
                filename=f"gen_{slug}_message.go",
                template_name="gen_message.go",
                context={"union": union},
            )
        )
        files.append(
            GoMessageFile(
                filename=f"gen_{slug}_constructors.go",
                template_name="gen_constructors.go",
                context={"union": union},
            )
        )

    for cases in message_cases:
        slug = go_message_file_slug(str(cases["name"]))
        files.append(
            GoMessageFile(
                filename=f"gen_{slug}_processor.go",
                template_name="gen_processor.go",
                context={"cases": cases},
            )
        )
        files.append(
            GoMessageFile(
                filename=f"gen_{slug}_visitor.go",
                template_name="gen_visitor.go",
                context={"cases": cases},
            )
        )
        variant_chunks = _chunk_variants(_as_variant_sequence(cases.get("variants")))
        if len(variant_chunks) == 1:
            files.append(
                GoMessageFile(
                    filename=f"gen_{slug}_cases.go",
                    template_name="gen_cases.go",
                    context={"cases": cases, "variants": variant_chunks[0]},
                )
            )
            continue
        for index, variants in enumerate(variant_chunks, start=1):
            files.append(
                GoMessageFile(
                    filename=f"gen_{slug}_cases_{index:03d}.go",
                    template_name="gen_cases.go",
                    context={"cases": cases, "variants": variants},
                )
            )
    return tuple(files)


def cleanup_stale_go_message_files(directory: Path, *, keep: set[str]) -> None:
    if not directory.is_dir():
        return
    for path in directory.iterdir():
        if not path.is_file() or path.name in keep:
            continue
        if not any(fnmatch(path.name, pattern) for pattern in _STALE_MESSAGE_FILE_PATTERNS):
            continue
        if _is_generated_file(path):
            path.unlink()


def _chunk_variants(variants: Sequence[Mapping[str, Any]]) -> tuple[tuple[Mapping[str, Any], ...], ...]:
    if not variants:
        return ((),)
    return tuple(
        tuple(variants[index : index + CASE_VARIANT_CHUNK_SIZE])
        for index in range(0, len(variants), CASE_VARIANT_CHUNK_SIZE)
    )


def _as_variant_sequence(value: object) -> tuple[Mapping[str, Any], ...]:
    if not isinstance(value, Sequence) or isinstance(value, (str, bytes)):
        return ()
    return tuple(item for item in value if isinstance(item, Mapping))


def _is_generated_file(path: Path) -> bool:
    try:
        with path.open("r", encoding="utf-8") as handle:
            return handle.readline().startswith(GENERATED_MARKER)
    except OSError:
        return False
