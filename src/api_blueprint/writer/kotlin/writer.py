from __future__ import annotations

import json
import logging
import shutil
from contextlib import contextmanager
from pathlib import Path
from typing import IO, TYPE_CHECKING, Generator, Optional, Sequence

from api_blueprint.engine.router import Router
from api_blueprint.writer.core.base import BaseWriter
from api_blueprint.writer.core.contract_adapters import RouteContractIndex, RouteProtocolContract
from api_blueprint.writer.core.errors import (
    ApiErrorEntry,
    ApiErrorGroup,
    api_errors_from_manifest,
    group_api_errors,
    route_api_errors_from_manifest,
)
from api_blueprint.writer.core.files import ensure_filepath_open
from api_blueprint.writer.core.templates import render

from .blueprint import KotlinBlueprint
from .naming import to_package_path
from .planner import build_kotlin_blueprint_plan
from .selection import normalize_rules

KOTLIN_GENERATED_HEADER = "// Code generated by api-blueprint; DO NOT EDIT.\n"

logging.basicConfig(level=logging.INFO, format="%(message)s")
logger = logging.getLogger("KotlinWriter")
logger.setLevel(logging.INFO)

if TYPE_CHECKING:
    from api_blueprint.contract import ContractGraph
    from .planner import KotlinBlueprintPlan, KotlinRouteGroupPlan


class KotlinWriter(BaseWriter[KotlinBlueprint]):
    def __init__(
        self,
        working_dir: str | Path = ".",
        *,
        package: str,
        base_url: str | None = None,
        base_url_expr: str | None = None,
        include: Sequence[str] = (),
        exclude: Sequence[str] = (),
        allow_empty: bool = False,
        contract_graph: ContractGraph | None = None,
    ):
        super().__init__(working_dir)
        self.package = package
        self.package_path = to_package_path(package)
        self.base_url = base_url or ""
        self.base_url_expr = base_url_expr
        self.rendered_base_url = base_url_expr if base_url_expr is not None else json.dumps(self.base_url)
        self.include = normalize_rules(include)
        self.exclude = normalize_rules(exclude)
        self.allow_empty = allow_empty
        self.contract_graph = contract_graph
        self.route_contract_index = RouteContractIndex.from_graph(contract_graph) if contract_graph is not None else None

    @property
    def package_dir(self) -> Path:
        return self.working_dir / self.package_path

    def root_dir(self, bp: KotlinBlueprint) -> Path:
        return self.package_dir / bp.root_package_path

    def gen(self) -> None:
        self.cleanup_legacy_layout()
        total_routes = 0
        for bp in self.bps:
            bp.build()
            bp.collect()
            total_routes += len(bp.routes)
            if not bp.routes:
                continue
            self._gen_blueprint(bp)
        if total_routes == 0 and not self.allow_empty:
            raise ValueError("[kotlin-client] Kotlin include/exclude 过滤后没有可生成的 route")

    def _ensure_route_contract_index(self) -> RouteContractIndex:
        if self.route_contract_index is None:
            from api_blueprint.contract import build_contract_graph

            self.contract_graph = build_contract_graph([bp.bp for bp in self.bps])
            self.route_contract_index = RouteContractIndex.from_graph(self.contract_graph)
        return self.route_contract_index

    def api_errors(self) -> tuple[ApiErrorEntry, ...]:
        if self.contract_graph is None:
            from api_blueprint.contract import build_contract_graph

            self.contract_graph = build_contract_graph([bp.bp for bp in self.bps])
            if self.route_contract_index is None:
                self.route_contract_index = RouteContractIndex.from_graph(self.contract_graph)
        return api_errors_from_manifest(self.contract_graph.to_manifest())

    def api_error_groups(self) -> tuple[ApiErrorGroup, ...]:
        return group_api_errors(self.api_errors())

    def api_errors_for_bp(self, bp: KotlinBlueprint) -> tuple[ApiErrorEntry, ...]:
        if self.contract_graph is None:
            self._ensure_route_contract_index()
        route_ids = [route.protocol.route.route_id for route in bp.routes]
        return api_errors_from_manifest(self.contract_graph.to_manifest(), route_ids=route_ids)

    def api_error_groups_for_bp(self, bp: KotlinBlueprint) -> tuple[ApiErrorGroup, ...]:
        return group_api_errors(self.api_errors_for_bp(bp))

    def route_api_errors_for_bp(self, bp: KotlinBlueprint) -> dict[str, tuple[ApiErrorEntry, ...]]:
        if self.contract_graph is None:
            self._ensure_route_contract_index()
        route_ids = [route.protocol.route.route_id for route in bp.routes]
        return route_api_errors_from_manifest(self.contract_graph.to_manifest(), route_ids=route_ids)

    def route_protocol_for(self, router: Router) -> RouteProtocolContract:
        return self._ensure_route_contract_index().protocol_for_router(router)

    def cleanup_legacy_layout(self) -> None:
        for legacy_name in ("endpoints", "internal", "models"):
            legacy_dir = self.package_dir / legacy_name
            if legacy_dir.exists():
                shutil.rmtree(legacy_dir)
        for legacy_name in ("ApiClient.kt", "ApiConfig.kt", "ApiException.kt"):
            legacy_file = self.package_dir / legacy_name
            if legacy_file.exists():
                legacy_file.unlink()

    def _gen_blueprint(self, bp: KotlinBlueprint) -> None:
        plan = build_kotlin_blueprint_plan(self, bp)
        self.cleanup_stale_generated_layout(plan)
        legacy_runtime_models_dir = plan.runtime.directory / "models"
        if legacy_runtime_models_dir.exists():
            shutil.rmtree(legacy_runtime_models_dir)
        for path in (plan.runtime.directory, plan.http_transport.directory):
            path.mkdir(parents=True, exist_ok=True)

        context = {"writer": self, "bp": bp}
        for output_name, template_name in plan.runtime.generated_files:
            with self.write_file(plan.runtime.directory / output_name, overwrite=True) as handle:
                if handle:
                    handle.write(KOTLIN_GENERATED_HEADER)
                    handle.write(render("kotlin", template_name, context, "runtime"))

        for output_name, template_name in plan.runtime.user_files:
            with self.write_file(plan.runtime.directory / output_name, overwrite=False) as handle:
                if handle:
                    handle.write(render("kotlin", template_name, context, "runtime"))

        plan.runtime.binary_runtime_file.parent.mkdir(parents=True, exist_ok=True)
        with self.write_file(plan.runtime.binary_runtime_file, overwrite=True) as handle:
            if handle:
                handle.write(KOTLIN_GENERATED_HEADER)
                handle.write(render("kotlin", "BinaryRuntime.kt", context, "runtime/binary"))

        for output_name, template_name in plan.http_transport.generated_files:
            with self.write_file(plan.http_transport.directory / output_name, overwrite=True) as handle:
                if handle:
                    handle.write(KOTLIN_GENERATED_HEADER)
                    handle.write(render("kotlin", template_name, context, "transports/http"))

        for output_name, template_name in plan.http_transport.user_files:
            with self.write_file(plan.http_transport.directory / output_name, overwrite=False) as handle:
                if handle:
                    handle.write(render("kotlin", template_name, context, "transports/http"))

        with self.write_file(plan.runtime.types_file, overwrite=True) as handle:
            if handle:
                handle.write(KOTLIN_GENERATED_HEADER)
                handle.write(render("kotlin", "Types.kt", {**context, "protos": plan.runtime.shared_protos}, "runtime"))

        for route_group in plan.route_groups:
            route_group.directory.mkdir(parents=True, exist_ok=True)
            group_models_context = {
                **context,
                "group": route_group.group,
                "protos": route_group.protos,
                "binary_schemas": route_group.group.binary_schemas(),
            }
            self._cleanup_stale_route_models(route_group)
            with self.write_file(route_group.types_file, overwrite=True) as handle:
                if handle:
                    handle.write(KOTLIN_GENERATED_HEADER)
                    handle.write(render("kotlin", "Types.kt", group_models_context, "routes"))

            with self.write_file(route_group.client_file, overwrite=True) as handle:
                if handle:
                    handle.write(KOTLIN_GENERATED_HEADER)
                    handle.write(render("kotlin", "ApiGroup.kt", {**context, "group": route_group.group}, "routes"))

            binary_schemas = route_group.group.binary_schemas()
            if binary_schemas:
                self._unlink_generated_file(route_group.stale_binary_file)
            self._cleanup_legacy_binary_dir(route_group.legacy_binary_directory)

            with self.write_file(route_group.facade_file, overwrite=False) as handle:
                if handle:
                    handle.write(render("kotlin", "ApiGroupFacade.kt", {**context, "group": route_group.group}, "routes"))

    def cleanup_stale_generated_layout(self, plan: "KotlinBlueprintPlan") -> None:
        stale_runtime_client = plan.runtime.directory / "ApiClient.kt"
        if self._is_stale_generated_runtime_client(stale_runtime_client):
            stale_runtime_client.unlink()

        for stale_file in (
            plan.runtime.directory / "ApiConfig.kt",
            plan.runtime.directory / "ApiException.kt",
            plan.runtime.directory / "ApiTransport.kt",
            plan.runtime.directory / "GenApiErrorCatalog.kt",
            plan.runtime.directory / "GenModels.kt",
            plan.runtime.directory / "Models.kt",
            plan.http_transport.directory / "HttpApiConfig.kt",
            plan.http_transport.directory / "OkHttpApiTransport.kt",
        ):
            if stale_file.exists():
                stale_file.unlink()

        routes_dir = plan.root_directory / "routes"
        for route_group in plan.route_groups:
            branch_only_dir = routes_dir / route_group.group.slug
            if branch_only_dir != route_group.directory and branch_only_dir.exists():
                shutil.rmtree(branch_only_dir)
            self._unlink_generated_file(route_group.stale_binary_file)
            self._cleanup_legacy_binary_dir(route_group.legacy_binary_directory)

    def _cleanup_legacy_binary_dir(self, binary_dir: Path) -> None:
        if not binary_dir.exists():
            return
        generated_file = binary_dir / "GenBinary.kt"
        if generated_file.exists():
            generated_file.unlink()
        try:
            binary_dir.rmdir()
        except OSError:
            return

    def _cleanup_stale_route_models(self, route_group: "KotlinRouteGroupPlan") -> None:
        directory = route_group.directory
        for stale_file in directory.glob("Gen*ApiModels.kt"):
            self._unlink_generated_file(stale_file)
        stale_models_file = directory / f"{route_group.group.class_name.removesuffix('Api')}Models.kt"
        self._unlink_generated_file(stale_models_file)

    def _unlink_generated_file(self, path: Path) -> None:
        if not path.exists():
            return
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            return
        if text.startswith(KOTLIN_GENERATED_HEADER):
            path.unlink()

    def _is_stale_generated_runtime_client(self, path: Path) -> bool:
        if not path.exists():
            return False
        text = path.read_text(encoding="utf-8")
        return (
            "public class ApiClient(" in text
            and "private val transport: ApiTransport" in text
            and ": GenApiClient(transport)" not in text
        )

    @contextmanager
    def write_file(self, filepath: str | Path, overwrite: bool = False) -> Generator[Optional[IO], None, None]:
        filepath_str = str(filepath)
        wrote = False
        with ensure_filepath_open(filepath_str, "w", overwrite=overwrite) as handle:
            if handle:
                wrote = True
            yield handle
        if wrote:
            logger.info("[+] Written: %s", filepath_str)
        else:
            logger.info("[.] Skipped: %s", filepath_str)
