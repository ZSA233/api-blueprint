JAVA_GENERATED_PREFIX = "// Code generated by api-blueprint"
JAVA_CLIENT_GENERATED_HEADER = "// Code generated by api-blueprint (Java client); DO NOT EDIT.\n"
JAVA_SERVER_GENERATED_HEADER = "// Code generated by api-blueprint (Java server); DO NOT EDIT.\n"

logging.basicConfig(level=logging.INFO, format="%(message)s")
logger = logging.getLogger("JavaWriter")
logger.setLevel(logging.INFO)


JsonObject = dict[str, Any]


class JavaBaseWriter(BaseWriter[JavaBlueprint]):
    target_label = "java"
    client_mode = False
    server_mode = False
    generated_header = JAVA_CLIENT_GENERATED_HEADER

    def __init__(
        self,
        working_dir: str | Path = ".",
        *,
        package: str,
        base_url: str | None = None,
        base_url_expr: str | None = None,
        include: Sequence[str] = (),
        exclude: Sequence[str] = (),
        allow_empty: bool = False,
        contract_graph: ContractGraph | None = None,
    ) -> None:
        super().__init__(working_dir)
        self.package = package
        self.package_path = to_package_path(package)
        self.base_url = base_url or ""
        self.base_url_expr = base_url_expr
        self.rendered_base_url = base_url_expr if base_url_expr is not None else json.dumps(self.base_url)
        self.include = normalize_rules(include)
        self.exclude = normalize_rules(exclude)
        self.allow_empty = allow_empty
        self.contract_graph = contract_graph
        self.catalog = JavaModelCatalog({})

    @property
    def package_dir(self) -> Path:
        return self.working_dir / self.package_path

    def root_dir(self, bp: JavaBlueprint) -> Path:
        return self.package_dir / bp.root_package_path

    def gen(self) -> None:
        graph = self.contract_graph or build_contract_graph([bp.bp for bp in self.bps])
        manifest = graph.to_manifest()
        schemas = _mapping_of_maps(manifest.get("schemas"))
        self.catalog = JavaModelCatalog(schemas)
        routes = [route for route in _list_of_maps(manifest.get("routes")) if self._route_selected(route)]
        services = _mapping_of_maps_by_id(manifest.get("services"))
        errors = api_errors_from_manifest(manifest)
        route_errors = route_api_errors_from_manifest(manifest, route_ids=[str(route.get("id") or "") for route in routes])

        total_routes = 0
        for bp in self.bps:
            bp.build()
            bp.collect_from_manifest(routes, services)
            total_routes += len(bp.routes)
            if not bp.routes:
                continue
            self._gen_blueprint(bp, schemas, errors, route_errors)
        if total_routes == 0 and not self.allow_empty:
            raise ValueError(f"[{self.target_label}] Java include/exclude 过滤后没有可生成的 route")

    def _route_selected(self, route: Mapping[str, Any]) -> bool:
        return JavaRouteSelection(include=self.include, exclude=self.exclude).includes(route)

    def _gen_blueprint(
        self,
        bp: JavaBlueprint,
        schemas: Mapping[str, JsonObject],
        errors: tuple[ApiErrorEntry, ...],
        route_errors: dict[str, tuple[ApiErrorEntry, ...]],
    ) -> None:
        plan = build_java_blueprint_plan(self, bp)
        self._cleanup_empty_binary_dirs(plan)
        context = {"writer": self, "bp": bp, "schemas": schemas, "errors": errors, "route_errors": route_errors}
        self._write_common_runtime(plan, context)
        if self.client_mode:
            self._write_client_runtime(plan, context)
            self._write_client_transport(plan, context)
        if self.server_mode:
            self._write_server_runtime(plan, context)
            self._write_server_transport(plan, context)
        for group_plan in plan.route_groups:
            group = group_plan.group
            self._write_group_models(group_plan, context)
            if self.client_mode:
                self._write_client_group(group_plan, context)
            if self.server_mode:
                self._write_server_group(group_plan, context)
            self._write_group_binary(group_plan, context)

    def _write_common_runtime(self, plan: JavaBlueprintPlan, context: dict[str, object]) -> None:
        for stale_name in ("ApiCodeError.java", "ApiErrorCatalogEntry.java", "GenApiErrorCatalog.java", "GenModels.java"):
            stale_path = plan.runtime.directory / stale_name
            if stale_path.exists():
                stale_path.unlink()
        for output_name, template_path in (
            ("ApiException.java", "ApiException.java"),
            ("ApiError.java", "ApiError.java"),
            ("ApiToastSpec.java", "ApiToastSpec.java"),
            ("ApiToastPayload.java", "ApiToastPayload.java"),
            ("ApiErrorEntry.java", "ApiErrorEntry.java"),
            ("ApiErrorPayload.java", "ApiErrorPayload.java"),
            ("ApiFilePart.java", "ApiFilePart.java"),
            ("ApiRawResponse.java", "ApiRawResponse.java"),
            ("ApiStreamResponse.java", "ApiStreamResponse.java"),
            ("ApiResponseEnvelope.java", "ApiResponseEnvelope.java"),
            ("ApiErrors.java", "ApiErrors.java"),
            ("ApiTypes.java", "ApiTypes.java"),
            ("ApiJson.java", "ApiJson.java"),
        ):
            self._write_generated(plan.runtime.directory / output_name, template_path, context, "common/runtime")
        self._write_generated(
            plan.runtime.binary_directory / "ApiBinaryBody.java",
            "ApiBinaryBody.java",
            context,
            "common/runtime/binary",
        )
        self._write_generated(
            plan.runtime.binary_directory / "GenBinaryRuntime.java",
            "GenBinaryRuntime.java",
            context,
            "common/runtime/binary",
        )

    def _write_client_runtime(self, plan: JavaBlueprintPlan, context: dict[str, object]) -> None:
        stale_socket_bridge = plan.runtime.directory / "ApiSocketBridge.java"
        if stale_socket_bridge.exists():
            stale_socket_bridge.unlink()
        for output_name, template_path in (
            ("ApiRequest.java", "ApiRequest.java"),
            ("ApiResponse.java", "ApiResponse.java"),
            ("ApiTransport.java", "ApiTransport.java"),
            ("ApiStreamBridge.java", "ApiStreamBridge.java"),
            ("ApiChannelBridge.java", "ApiChannelBridge.java"),
            ("GenApiClient.java", "GenApiClient.java"),
        ):
            self._write_generated(plan.runtime.directory / output_name, template_path, context, "client/runtime")
        self._write_user_file(plan.runtime.directory / "ApiClient.java", "ApiClient.java", context, "client/runtime")

    def _write_client_transport(self, plan: JavaBlueprintPlan, context: dict[str, object]) -> None:
        self._write_generated(
            plan.http_transport.directory / "GenHttpApiConfig.java",
            "GenHttpApiConfig.java",
            context,
            "client/transports/http",
        )
        self._write_generated(
            plan.http_transport.directory / "GenJdkHttpApiTransport.java",
            "GenJdkHttpApiTransport.java",
            context,
            "client/transports/http",
        )
        self._write_user_file(
            plan.http_transport.directory / "HttpApiClient.java",
            "HttpApiClient.java",
            context,
            "client/transports/http",
        )

    def _write_server_runtime(self, plan: JavaBlueprintPlan, context: dict[str, object]) -> None:
        self._write_generated(
            plan.runtime.directory / "ApiServerContext.java",
            "ApiServerContext.java",
            context,
            "server/runtime",
        )
        self._write_generated(
            plan.runtime.directory / "ApiServerResponse.java",
            "ApiServerResponse.java",
            context,
            "server/runtime",
        )
        self._write_generated(
            plan.runtime.directory / "ApiServerStream.java",
            "ApiServerStream.java",
            context,
            "server/runtime",
        )
        self._write_generated(
            plan.runtime.directory / "ApiServerChannel.java",
            "ApiServerChannel.java",
            context,
            "server/runtime",
        )

    def _write_server_transport(self, plan: JavaBlueprintPlan, context: dict[str, object]) -> None:
        self._write_generated(
            plan.http_transport.directory / "GenSpringApiConfiguration.java",
            "GenSpringApiConfiguration.java",
            context,
            "server/transports/http",
        )
        for group in context["bp"].groups.values():  # type: ignore[union-attr]
            self._write_generated(
                plan.http_transport.directory / group.package_path / f"{group.controller_class}.java",
                "GenSpringController.java",
                {**context, "group": group},
                "server/transports/http",
            )

    def _write_group_models(self, group_plan: JavaRouteGroupPlan, context: dict[str, object]) -> None:
        group = group_plan.group
        for stale_file in (*group_plan.directory.glob("Gen*ApiModels.java"), *group_plan.directory.glob("Gen*ServiceModels.java")):
            self._unlink_generated_file(stale_file)
        stale_models_file = group_plan.directory / f"{group.types_class.removesuffix('Types')}Models.java"
        self._unlink_generated_file(stale_models_file)
        self._write_generated(
            group_plan.directory / f"{group.types_class}.java",
            "ApiGroupTypes.java",
            {**context, "group": group},
            "client/routes" if self.client_mode else "server/routes",
        )

    def _write_client_group(self, group_plan: JavaRouteGroupPlan, context: dict[str, object]) -> None:
        group = group_plan.group
        self._write_generated(
            group_plan.directory / f"{group.generated_client_class}.java",
            "GenApiGroup.java",
            {**context, "group": group},
            "client/routes",
        )
        self._write_user_file(
            group_plan.directory / f"{group.class_name}.java",
            "ApiGroup.java",
            {**context, "group": group},
            "client/routes",
        )

    def _write_server_group(self, group_plan: JavaRouteGroupPlan, context: dict[str, object]) -> None:
        group = group_plan.group
        for output_name, template_name, overwrite in (
            (f"{group.generated_service_class}.java", "GenApiService.java", True),
            (f"{group.stub_class}.java", "GenApiServiceStub.java", True),
            (f"{group.service_class}.java", "ApiService.java", False),
        ):
            if overwrite:
                self._write_generated(group_plan.directory / output_name, template_name, {**context, "group": group}, "server/routes")
            else:
                self._write_user_file(group_plan.directory / output_name, template_name, {**context, "group": group}, "server/routes")

    def _write_group_binary(self, group_plan: JavaRouteGroupPlan, context: dict[str, object]) -> None:
        self._unlink_generated_file(group_plan.stale_binary_file)
        self._cleanup_legacy_binary_dir(group_plan.legacy_binary_directory)

    def _cleanup_empty_binary_dirs(self, plan: JavaBlueprintPlan) -> None:
        for group_plan in plan.route_groups:
            self._unlink_generated_file(group_plan.stale_binary_file)
            self._cleanup_legacy_binary_dir(group_plan.legacy_binary_directory)

    def _cleanup_legacy_binary_dir(self, binary_dir: Path) -> None:
        if not binary_dir.exists():
            return
        generated_file = binary_dir / "GenBinary.java"
        if generated_file.exists():
            generated_file.unlink()
        try:
            binary_dir.rmdir()
        except OSError:
            return

    def _unlink_generated_file(self, path: Path) -> None:
        if not path.exists():
            return
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            return
        if text.startswith(JAVA_GENERATED_PREFIX):
            path.unlink()

    def route_params(self, route: JavaRoute, group: JavaApiGroup) -> list[tuple[str, str]]:
        params: list[tuple[str, str]] = []
        if self.server_mode and route.kind == "stream":
            if route.open_model:
                params.append((self.schema_type(route.open_model, group), "openData"))
            params.append((f"ApiServerStream<{self.server_message_type(route, group)}, {self.close_type(route, group)}>", "stream"))
            return params
        if self.server_mode and route.kind == "channel":
            if route.open_model:
                params.append((self.schema_type(route.open_model, group), "openData"))
            params.append(
                (
                    f"ApiServerChannel<{self.client_message_type(route, group)}, {self.server_message_type(route, group)}, {self.close_type(route, group)}>",
                    "channel",
                )
            )
            return params
        if route.query_model:
            params.append((self.schema_type(route.query_model, group), "query"))
        if route.json_model:
            params.append((self.schema_type(route.json_model, group), "json"))
        if route.form_model:
            params.append((self.schema_type(route.form_model, group), "form"))
        if route.multipart_model:
            params.append((self.schema_type(route.multipart_model, group), "multipart"))
        if route.binary_schema is not None:
            if self.server_mode:
                params.append((f"{group.types_class}.{route.binary_schema_type}", "binary"))
            else:
                params.append(("ApiBinaryBody", "binaryBody"))
        elif route.binary_model:
            params.append(("byte[]", "binaryBody") if self.server_mode else (self.schema_type(route.binary_model, group), "binaryBody"))
        if route.open_model:
            params.append((self.schema_type(route.open_model, group), "openData"))
        return params

    def schema_type(self, schema_name: str, group: JavaApiGroup | None = None) -> str:
        return self.catalog.type_for_schema_name(schema_name, owner_group=group)

    def schema_class_literal(self, schema_name: str, group: JavaApiGroup | None = None) -> str:
        return self.catalog.class_literal_for_schema_name(schema_name, owner_group=group)

    def response_type(self, route: JavaRoute, group: JavaApiGroup) -> str:
        if self.server_mode and not route.is_rpc:
            return "void"
        if route.is_binary_schema_response and route.response_binary_schema_type:
            return f"{group.types_class}.{route.response_binary_schema_type}"
        if route.response_kind in {"bytes", "file"}:
            return "ApiRawResponse"
        if route.response_kind == "byte_stream":
            return "ApiStreamResponse"
        if route.response_model:
            return self.schema_type(route.response_model, group)
        if route.response_media_type != "application/json":
            return "String"
        return "Object"

    def close_type(self, route: JavaRoute, group: JavaApiGroup) -> str:
        if not route.close_model or route.close_model.rsplit(".", 1)[-1] == "DefaultConnectionClose":
            return "Object"
        return self.schema_type(route.close_model, group)

    def response_class_literal(self, route: JavaRoute, group: JavaApiGroup) -> str:
        if route.is_binary_schema_response and route.response_binary_schema_type:
            return f"{group.types_class}.{route.response_binary_schema_type}.class"
        if route.response_kind in {"bytes", "file"}:
            return "ApiRawResponse.class"
        if route.response_kind == "byte_stream":
            return "ApiStreamResponse.class"
        if route.response_model:
            return self.schema_class_literal(route.response_model, group)
        if route.response_media_type != "application/json":
            return "String.class"
        return "Object.class"

    def server_message_type(self, route: JavaRoute, group: JavaApiGroup) -> str:
        return self._connection_message_type(route, group, "server_message")

    def client_message_type(self, route: JavaRoute, group: JavaApiGroup) -> str:
        return self._connection_message_type(route, group, "client_message")

    def _connection_message_type(self, route: JavaRoute, group: JavaApiGroup, key: str) -> str:
        message = route.connection.get(key)
        if not isinstance(message, Mapping):
            return "Object"
        variants = message.get("variants")
        if isinstance(variants, list) and variants:
            has_named_variants = any(
                isinstance(variant, Mapping) and bool(str(variant.get("key") or ""))
                for variant in variants
            )
            if has_named_variants:
                name = route.server_message_name if key == "server_message" else route.client_message_name
                if name:
                    return f"{group.types_class}.{name}"
                return "Object"
            first_variant = variants[0]
            if isinstance(first_variant, Mapping):
                model = first_variant.get("model")
                if isinstance(model, str) and model:
                    return self.schema_type(model, group)
        model = message.get("model")
        if isinstance(model, str) and model:
            return self.schema_type(model, group)
        return "Object"

    def model_schemas_for_group(self, group: JavaApiGroup) -> tuple[object, ...]:
        auto_names = {name for name in group.model_names() if self.catalog.is_auto_schema(name)}
        return self.catalog.route_schemas(auto_names, owner_group=group)

    def shared_schemas(self) -> tuple[object, ...]:
        return self.catalog.shared_schemas()

    def enums(self) -> tuple[object, ...]:
        return self.catalog.enums()

    def constant_name(self, value: str) -> str:
        return to_java_constant_name(value)

    def _write_generated(self, filepath: Path, template_name: str, context: dict[str, object], relative_path: str) -> None:
        with self.write_file(filepath, overwrite=True) as handle:
            if handle:
                handle.write(self.generated_header)
                handle.write(render("java", template_name, context, relative_path))

    def _write_user_file(self, filepath: Path, template_name: str, context: dict[str, object], relative_path: str) -> None:
        with self.write_file(filepath, overwrite=False) as handle:
            if handle:
                handle.write(render("java", template_name, context, relative_path))

    @contextmanager
    def write_file(self, filepath: str | Path, overwrite: bool = False) -> Generator[IO[str] | None, None, None]:
        filepath_str = str(filepath)
        wrote = False
        with ensure_filepath_open(filepath_str, "w", overwrite=overwrite) as handle:
            if handle:
                wrote = True
            yield handle
        if wrote:
            logger.info("[+] Written: %s", filepath_str)
        else:
            logger.info("[.] Skipped: %s", filepath_str)


def _list_of_maps(value: object) -> list[JsonObject]:
    if not isinstance(value, list):
        return []
    return [dict(item) for item in value if isinstance(item, Mapping)]


def _mapping_of_maps(value: object) -> dict[str, JsonObject]:
    if not isinstance(value, Mapping):
        return {}
    return {str(key): dict(item) for key, item in value.items() if isinstance(item, Mapping)}


def _mapping_of_maps_by_id(value: object) -> dict[str, JsonObject]:
    result: dict[str, JsonObject] = {}
    for item in _list_of_maps(value):
        item_id = item.get("id")
        if isinstance(item_id, str):
            result[item_id] = item
    return result
