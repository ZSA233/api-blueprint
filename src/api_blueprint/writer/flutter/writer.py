from __future__ import annotations

import json
import logging
from contextlib import contextmanager
from dataclasses import dataclass
from pathlib import Path
from typing import IO, TYPE_CHECKING, Generator, Optional, Sequence

from api_blueprint.engine.router import Router
from api_blueprint.writer.core.base import BaseWriter
from api_blueprint.writer.core.contract_adapters import RouteContractIndex, RouteProtocolContract
from api_blueprint.writer.core.errors import (
    ApiErrorEntry,
    ApiErrorGroup,
    api_errors_from_manifest,
    group_api_errors,
    route_api_errors_from_manifest,
)
from api_blueprint.writer.core.files import ensure_filepath_open
from api_blueprint.writer.core.templates import render

from .blueprint import FlutterBlueprint
from .naming import to_dart_identifier, to_dart_package_name
from .planner import build_flutter_blueprint_plan
from .selection import FlutterRouteSelection

if TYPE_CHECKING:
    from api_blueprint.contract import ContractGraph
    from .planner import FlutterRouteGroupPlan


FLUTTER_GENERATED_PREFIX = "// Code generated by api-blueprint"
FLUTTER_CLIENT_GENERATED_HEADER = "// Code generated by api-blueprint (Flutter client); DO NOT EDIT.\n"

logging.basicConfig(level=logging.INFO, format="%(message)s")
logger = logging.getLogger("FlutterWriter")
logger.setLevel(logging.INFO)


@dataclass(frozen=True)
class FlutterApiErrorConstant:
    name: str
    code: int
    entry: ApiErrorEntry


class FlutterWriter(BaseWriter[FlutterBlueprint]):
    target_label = "flutter-client"

    def __init__(
        self,
        working_dir: str | Path = ".",
        *,
        package: str,
        base_url: str | None = None,
        base_url_expr: str | None = None,
        include: Sequence[str] = (),
        exclude: Sequence[str] = (),
        allow_empty: bool = False,
        contract_graph: ContractGraph | None = None,
    ):
        super().__init__(working_dir)
        self.package = to_dart_package_name(package)
        self.base_url = base_url or ""
        self.base_url_expr = base_url_expr
        self.rendered_base_url = base_url_expr if base_url_expr is not None else json.dumps(self.base_url)
        self.selection = FlutterRouteSelection(include=include, exclude=exclude)
        self.allow_empty = allow_empty
        self.contract_graph = contract_graph
        self.route_contract_index = RouteContractIndex.from_graph(contract_graph) if contract_graph is not None else None

    @property
    def lib_dir(self) -> Path:
        return self.working_dir / "lib"

    @property
    def source_dir(self) -> Path:
        return self.lib_dir / "src"

    def gen(self) -> None:
        self.working_dir.mkdir(parents=True, exist_ok=True)
        self._write_package_scaffold()
        total_routes = 0
        generated_bps: list[FlutterBlueprint] = []
        for bp in self.bps:
            bp.build()
            bp.collect()
            total_routes += len(bp.routes)
            if not bp.routes:
                continue
            self._gen_blueprint(bp)
            generated_bps.append(bp)
        if total_routes == 0 and not self.allow_empty:
            raise ValueError(f"[{self.target_label}] Flutter include/exclude 过滤后没有可生成的 route")
        if generated_bps:
            self._write_public_entry(generated_bps)

    def includes(self, router: Router, *, route_name: str = "") -> bool:
        return self.selection.includes(router, route_name=route_name)

    def _ensure_route_contract_index(self) -> RouteContractIndex:
        if self.route_contract_index is None:
            from api_blueprint.contract import build_contract_graph

            self.contract_graph = build_contract_graph([bp.bp for bp in self.bps])
            self.route_contract_index = RouteContractIndex.from_graph(self.contract_graph)
        return self.route_contract_index

    def route_protocol_for(self, router: Router) -> RouteProtocolContract:
        return self._ensure_route_contract_index().protocol_for_router(router)

    def api_errors(self) -> tuple[ApiErrorEntry, ...]:
        if self.contract_graph is None:
            from api_blueprint.contract import build_contract_graph

            self.contract_graph = build_contract_graph([bp.bp for bp in self.bps])
            if self.route_contract_index is None:
                self.route_contract_index = RouteContractIndex.from_graph(self.contract_graph)
        return api_errors_from_manifest(self.contract_graph.to_manifest())

    def api_error_groups(self) -> tuple[ApiErrorGroup, ...]:
        return group_api_errors(self.api_errors())

    def api_errors_for_bp(self, bp: FlutterBlueprint) -> tuple[ApiErrorEntry, ...]:
        if self.contract_graph is None:
            self._ensure_route_contract_index()
        route_ids = [route.protocol.route.route_id for route in bp.routes]
        return api_errors_from_manifest(self.contract_graph.to_manifest(), route_ids=route_ids)

    def api_error_groups_for_bp(self, bp: FlutterBlueprint) -> tuple[ApiErrorGroup, ...]:
        return group_api_errors(self.api_errors_for_bp(bp))

    def route_api_errors_for_bp(self, bp: FlutterBlueprint) -> dict[str, tuple[ApiErrorEntry, ...]]:
        if self.contract_graph is None:
            self._ensure_route_contract_index()
        route_ids = [route.protocol.route.route_id for route in bp.routes]
        return route_api_errors_from_manifest(self.contract_graph.to_manifest(), route_ids=route_ids)

    def error_const_name(self, entry: ApiErrorEntry) -> str:
        parts = [part for part in entry.key.lower().split("_") if part]
        if not parts:
            return "code"
        return parts[0] + "".join(part[:1].upper() + part[1:] for part in parts[1:])

    def api_error_constants(self) -> tuple[FlutterApiErrorConstant, ...]:
        entries = list(self.api_errors())
        base_names = [self.error_const_name(entry) for entry in entries]
        duplicate_bases = {name for name in base_names if base_names.count(name) > 1}
        used: set[str] = set()
        constants: list[FlutterApiErrorConstant] = []
        for entry, base_name in zip(entries, base_names):
            name = base_name
            if name in duplicate_bases:
                name = self._grouped_error_const_name(entry)
            unique_name = name
            suffix = 2
            while unique_name in used:
                unique_name = f"{name}{suffix}"
                suffix += 1
            used.add(unique_name)
            constants.append(FlutterApiErrorConstant(name=unique_name, code=entry.code, entry=entry))
        return tuple(constants)

    def _grouped_error_const_name(self, entry: ApiErrorEntry) -> str:
        group = entry.group or "api"
        key = entry.key or "code"
        return to_dart_identifier(f"{group}_{key}", fallback="apiErrorCode")

    def dart_string_literal(self, value: str) -> str:
        return json.dumps(value, ensure_ascii=False).replace("$", r"\$")

    def _write_package_scaffold(self) -> None:
        pubspec = self.working_dir / "pubspec.yaml"
        with self.write_file(pubspec, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "pubspec.yaml", {"writer": self}))
        analysis = self.working_dir / "analysis_options.yaml"
        with self.write_file(analysis, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "analysis_options.yaml", {"writer": self}))

    def _write_public_entry(self, bps: Sequence[FlutterBlueprint]) -> None:
        package_facade = self.lib_dir / f"{self.package}.dart"
        self._unlink_generated_file(package_facade)
        with self.write_file(package_facade, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "public_entry.dart", {"writer": self, "primary_bp": None}))
        with self.write_file(self.lib_dir / f"gen_{self.package}.dart", overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                primary_bp = bps[0] if bps else None
                handle.write(render("flutter", "gen_public_entry.dart", {"writer": self, "primary_bp": primary_bp}))
        for bp in bps:
            public_name = bp.root_path.rsplit("/", 1)[-1]
            root_facade = self.lib_dir / f"{public_name}.dart"
            self._unlink_generated_file(root_facade)
            with self.write_file(root_facade, overwrite=False) as handle:
                if handle:
                    handle.write(render("flutter", "root_public_entry.dart", {"writer": self, "bp": bp}))
            with self.write_file(self.lib_dir / f"gen_{public_name}.dart", overwrite=True) as handle:
                if handle:
                    handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                    handle.write(render("flutter", "gen_root_public_entry.dart", {"writer": self, "bp": bp}))

    def _gen_blueprint(self, bp: FlutterBlueprint) -> None:
        plan = build_flutter_blueprint_plan(self, bp)
        plan.root_directory.mkdir(parents=True, exist_ok=True)
        plan.runtime.directory.mkdir(parents=True, exist_ok=True)
        plan.http_transport.directory.mkdir(parents=True, exist_ok=True)
        context = {"writer": self, "bp": bp, "plan": plan}

        self._unlink_generated_file(plan.root_facade_file)
        with self.write_file(plan.root_facade_file, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "root_public_entry.dart", {"writer": self, "bp": bp}))

        with self.write_file(plan.root_barrel_file, overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                handle.write(render("flutter", "gen_root_barrel.dart", context))

        for output_name, template_name in plan.runtime.generated_files:
            with self.write_file(plan.runtime.directory / output_name, overwrite=True) as handle:
                if handle:
                    handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                    handle.write(render("flutter", template_name, context, "runtime"))

        for output_name, template_name in plan.runtime.user_files:
            with self.write_file(plan.runtime.directory / output_name, overwrite=False) as handle:
                if handle:
                    handle.write(render("flutter", template_name, context, "runtime"))

        with self.write_file(plan.runtime.types_file, overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                handle.write(render("flutter", "gen_api_types.dart", {**context, "protos": plan.runtime.shared_protos}, "runtime"))

        plan.runtime.binary_runtime_file.parent.mkdir(parents=True, exist_ok=True)
        with self.write_file(plan.runtime.binary_runtime_file, overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                handle.write(render("flutter", "gen_binary_runtime.dart", context, "runtime/binary"))

        for output_name, template_name in plan.http_transport.generated_files:
            with self.write_file(plan.http_transport.directory / output_name, overwrite=True) as handle:
                if handle:
                    handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                    handle.write(render("flutter", template_name, context, "transports/http"))

        for output_name, template_name in plan.http_transport.user_files:
            with self.write_file(plan.http_transport.directory / output_name, overwrite=False) as handle:
                if handle:
                    handle.write(render("flutter", template_name, context, "transports/http"))

        for route_group in plan.route_groups:
            self._gen_route_group(bp, plan, route_group)

    def _gen_route_group(
        self,
        bp: FlutterBlueprint,
        plan: "FlutterBlueprintPlan",
        route_group: "FlutterRouteGroupPlan",
    ) -> None:
        route_group.directory.mkdir(parents=True, exist_ok=True)
        context = {"writer": self, "bp": bp, "plan": plan, "group": route_group.group, "route_group": route_group}
        with self.write_file(route_group.client_file, overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                handle.write(render("flutter", "gen_api.dart", context, "routes"))
        with self.write_file(route_group.facade_file, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "api.dart", context, "routes"))
        with self.write_file(route_group.types_file, overwrite=True) as handle:
            if handle:
                handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                handle.write(
                    render(
                        "flutter",
                        "gen_types.dart",
                        {**context, "protos": route_group.protos, "binary_schemas": route_group.group.binary_schemas()},
                        "routes",
                    )
                )
        with self.write_file(route_group.types_facade_file, overwrite=False) as handle:
            if handle:
                handle.write(render("flutter", "types.dart", context, "routes"))
        if route_group.group.binary_schemas():
            with self.write_file(route_group.binary_file, overwrite=True) as handle:
                if handle:
                    handle.write(FLUTTER_CLIENT_GENERATED_HEADER)
                    handle.write(
                        render(
                            "flutter",
                            "gen_binary.dart",
                            {**context, "binary_schemas": route_group.group.binary_schemas()},
                            "routes",
                        )
                    )
            with self.write_file(route_group.binary_facade_file, overwrite=False) as handle:
                if handle:
                    handle.write(render("flutter", "binary.dart", context, "routes"))

    @contextmanager
    def write_file(self, filepath: str | Path, overwrite: bool = False) -> Generator[Optional[IO], None, None]:
        filepath_str = str(filepath)
        wrote = False
        with ensure_filepath_open(filepath_str, "w", overwrite=overwrite) as handle:
            if handle:
                wrote = True
            yield handle
        if wrote:
            logger.info("[+] Written: %s", filepath_str)
        else:
            logger.info("[.] Skipped: %s", filepath_str)

    def _unlink_generated_file(self, path: Path) -> None:
        if not path.exists():
            return
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            return
        if text.startswith(FLUTTER_GENERATED_PREFIX):
            path.unlink()
