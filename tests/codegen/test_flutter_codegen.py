def test_flutter_writer_exports_multiple_roots(tmp_path: Path) -> None:
    class Result(Model):
        ok = String(description="ok")

    api_bp = Blueprint(root="/api")
    api_bp.GET("/ping").RSP(Result)
    static_bp = Blueprint(root="/static")
    static_bp.GET("/doc").RSP(Result)

    out_dir = tmp_path / "flutter"
    writer = FlutterWriter(out_dir, package="api_blueprint_example")
    writer.register(api_bp)
    writer.register(static_bp)
    writer.gen()

    public_entry = (out_dir / "lib" / "api_blueprint_example.dart").read_text(encoding="utf-8")
    api_entry = (out_dir / "lib" / "api.dart").read_text(encoding="utf-8")
    static_entry = (out_dir / "lib" / "static_.dart").read_text(encoding="utf-8")

    assert "export 'src/api/api.dart';" in public_entry
    assert "export 'src/static_/static_.dart';" not in public_entry
    assert "export 'src/api/api.dart';" in api_entry
    assert "export 'src/static_/static_.dart';" in static_entry


def test_flutter_error_lookup_preserves_dynamic_payload_and_unique_error_constants(tmp_path: Path) -> None:
    class CommonErr(Model):
        UNKNOWN = Error(40000, "common unknown")

    class DemoErr(Model):
        UNKNOWN = Error(40001, "demo unknown")

    class Result(Model):
        ok = String(description="ok")

    bp = Blueprint(root="/api", errors=[CommonErr])
    bp.GET("/demo").RSP(Result).ERR(DemoErr.UNKNOWN)

    out_dir = tmp_path / "flutter"
    writer = FlutterWriter(out_dir, package="api_blueprint_example")
    writer.register(bp)
    writer.gen()

    lookup_text = (
        out_dir / "lib" / "src" / "api" / "runtime" / "gen_api_error_lookup.dart"
    ).read_text(encoding="utf-8")

    assert "static const commonErrUnknown = 40000;" in lookup_text
    assert "static const demoErrUnknown = 40001;" in lookup_text
    assert "ApiErrorPayload mergeApiErrorPayload" in lookup_text
    assert "message: payload.message.isNotEmpty ? payload.message : known.message" in lookup_text


def test_flutter_writer_generates_binary_schema_codecs(tmp_path: Path) -> None:
    schema = parse_binary_schema(
        """
# packet DemoPacket

endian: little
content-type: application/octet-stream
content-encoding: identity,gzip

## header

| field | type | count | rule | comment |
|---|---|---:|---|---|
| magic | bytes | 4 | const="ABP1" | magic |
| version | u16 | 1 | const=1 | version |
| item_count | u16 | 1 | min=1,max=8,sizeof=items | item count |

## body

| field | type | count | rule | comment |
|---|---|---:|---|---|
| items | Item | item_count | | items |

## struct Item

| field | type | count | rule | comment |
|---|---|---:|---|---|
| id | u32 | 1 | max=100 | id |
""".strip()
    )

    bp = Blueprint(root="/api")
    with bp.group("/binary") as views:
        views.POST("/packet").REQ_BINARY(schema).RSP(ok=String(description="ok"))

    writer = FlutterWriter(tmp_path / "flutter", package="api_blueprint_example")
    writer.register(bp)
    writer.gen()

    route_dir = tmp_path / "flutter" / "lib" / "src" / "api" / "routes" / "api" / "binary"
    runtime_dir = tmp_path / "flutter" / "lib" / "src" / "api" / "runtime"
    binary_text = (route_dir / "gen_binary.dart").read_text(encoding="utf-8")
    runtime_text = (runtime_dir / "binary" / "gen_binary_runtime.dart").read_text(encoding="utf-8")
    route_text = (route_dir / "gen_binary_api.dart").read_text(encoding="utf-8")

    assert "class DemoPacket" in binary_text
    assert "class DemoPacketItem" in binary_text
    assert "Uint8List encodeDemoPacket(DemoPacket value)" in binary_text
    assert "DemoPacket decodeDemoPacket(Uint8List bytes)" in binary_text
    assert "class ApiBinaryWriter" in runtime_text
    assert "binary: encodeDemoPacket(packet)" in route_text
