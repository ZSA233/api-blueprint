class WireEnum(enum.StrEnum):
    FIRST = "first"
    SECOND = "second"


class Payload(Model):
    user_id = String(alias="user_id", description="user id")
    scores = Array[Int64](description="scores")
    kind = Enum[WireEnum](description="kind")


class Result(Model):
    status = String(description="status")


