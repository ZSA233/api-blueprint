def test_java_model_catalog_builds_record_fields_and_enum_metadata() -> None:
    bp = Blueprint(root="/api")
    with bp.group("/demo") as views:
        views.POST("/submit").REQ(Payload).RSP(Result)

    manifest = build_contract_graph([bp]).to_manifest()
    catalog = JavaModelCatalog(manifest["schemas"])
    schema = catalog.schema("Payload", owner_group=None)
    enum_type = catalog.enums()[0]

    assert schema.name == "Payload"
    assert [field.java_name for field in schema.fields] == ["userId", "scores", "kind"]
    assert [field.wire_name for field in schema.fields] == ["user_id", "scores", "kind"]
    assert [field.java_type for field in schema.fields] == ["String", "List<Long>", "ApiTypes.WireEnum"]
    assert enum_type.name == "WireEnum"
    assert [member.java_name for member in enum_type.values] == ["FIRST", "SECOND"]
    assert [member.literal for member in enum_type.values] == ['"first"', '"second"']


def test_java_selection_matches_path_tag_group_method_and_name(example_entrypoints) -> None:
    _config, entrypoints = example_entrypoints
    graph = build_contract_graph(entrypoints)
    route = next(route for route in graph.to_manifest()["routes"] if route["url"] == "/api/demo/abc")

    assert JavaRouteSelection(include=("path:/api/demo/*",)).includes(route)
    assert JavaRouteSelection(include=("tag:api",)).includes(route)
    assert JavaRouteSelection(include=("group:demo",)).includes(route)
    assert JavaRouteSelection(include=("method:GET",)).includes(route)
    assert JavaRouteSelection(include=("name:Abc",)).includes(route)
    assert not JavaRouteSelection(include=("tag:api",), exclude=("path:/api/demo/*",)).includes(route)


