class Payload(Model):
    value = String(description="value")


class OpenPayload(Model):
    token = String(description="token")


class StreamMessage(Model):
    message = String(description="message")


class ClientMessage(Model):
    message = String(description="message")


class CloseInfo(Model):
    reason = String(description="reason")


def _ownership_blueprint() -> Blueprint:
    bp = Blueprint(root="/api")
    with bp.group("/demo") as views:
        views.GET("/ping").ARGS(q=String(description="q")).RSP(Payload)
        views.STREAM("/events").OPEN(OpenPayload).SERVER_MESSAGE(StreamMessage).CLOSE(CloseInfo)
        views.CHANNEL("/chat", delivery=ConnectionDelivery.UNORDERED).OPEN(OpenPayload).CLIENT_MESSAGE(
            ClientMessage
        ).SERVER_MESSAGE(StreamMessage).CLOSE(CloseInfo)
    return bp


def _has_generated_marker(path: Path) -> bool:
    try:
        text = path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        return False
    return GENERATED_MARKER in "\n".join(text.splitlines()[:5])


def _is_protoc_output(path: Path) -> bool:
    name = path.name
    return name.endswith(".pb.go") or name.endswith("_pb2.py") or name.endswith("_pb2_grpc.py")


def _is_owned_generated_path(path: Path) -> bool:
    return (
        path.name in {"Package.swift", "Package.swift.j2"}
        or path.name in {"ApiBlueprintOperation.java", "ApiBlueprintOperation.java.j2"}
        or path.name.startswith("gen_")
        or path.name.startswith("Gen")
        or any(part.startswith("_gen_") for part in path.parts)
    )


def _ownership_violations(root: Path) -> list[str]:
    violations: list[str] = []
    for path in sorted(item for item in root.rglob("*") if item.is_file()):
        if not _has_generated_marker(path) or _is_protoc_output(path):
            continue
        if not _is_owned_generated_path(path.relative_to(root)):
            violations.append(path.relative_to(root).as_posix())
    return violations


def _template_ownership_violations() -> list[str]:
    violations: list[str] = []
    for path in sorted(TEMPLATE_ROOT.rglob("*.j2")):
        relative = path.relative_to(TEMPLATE_ROOT)
        relative_text = relative.as_posix()
        if _is_owned_generated_path(relative):
            continue
        text = path.read_text(encoding="utf-8")
        if GENERATED_MARKER in "\n".join(text.splitlines()[:5]):
            violations.append(relative_text)
            continue
        if relative.parts[0] in STRICT_TEMPLATE_ROOTS and relative_text not in PRESERVED_TEMPLATE_PATHS:
            violations.append(relative_text)
    return violations


def test_generated_templates_use_owned_names() -> None:
    assert _template_ownership_violations() == []


@pytest.mark.parametrize(
    ("label", "generate"),
    [
        ("go-client", lambda output, bp, graph: GolangClientWriter(output, module="example.com/generated/client", contract_graph=graph)),
        ("go-server", lambda output, bp, graph: GolangWriter(output, module="example.com/generated", contract_graph=graph)),
        ("typescript", lambda output, bp, graph: TypeScriptWriter(output, contract_graph=graph)),
        (
            "typescript-wails",
            lambda output, bp, graph: TypeScriptWriter(
                output,
                transport_kind="wails-v3",
                overlay_name="wailsv3",
                frontend_mode="external",
                wails_binding_manifest={},
                contract_graph=graph,
            ),
        ),
        ("python-client", lambda output, bp, graph: PythonClientWriter(output, contract_graph=graph)),
        ("python-server", lambda output, bp, graph: PythonServerWriter(output, contract_graph=graph)),
        ("java-client", lambda output, bp, graph: JavaClientWriter(output, package="com.example.generated", contract_graph=graph)),
        ("java-server", lambda output, bp, graph: JavaServerWriter(output, package="com.example.generated", contract_graph=graph)),
        ("kotlin-client", lambda output, bp, graph: KotlinWriter(output, package="com.example.generated", contract_graph=graph)),
        ("kotlin-server", lambda output, bp, graph: KotlinServerWriter(output, package="com.example.generated", contract_graph=graph)),
        ("flutter", lambda output, bp, graph: FlutterWriter(output, package="api_blueprint_example", contract_graph=graph)),
        ("swift", lambda output, bp, graph: SwiftWriter(output, package="ApiBlueprintExampleClient", contract_graph=graph)),
        (
            "wails-go",
            lambda output, bp, graph: WailsGoWriter(
                output,
                version="v3",
                overlay_name="wailsv3",
                module="example.com/generated",
                contract_graph=graph,
            ),
        ),
    ],
)
def test_generated_files_use_owned_names(tmp_path: Path, label: str, generate: Callable[[Path, Blueprint, object], object]) -> None:
    bp = _ownership_blueprint()
    graph = build_contract_graph([bp])
    output_dir = tmp_path / label
    output_dir.mkdir(parents=True)
    if label in {"go-server", "wails-go"}:
        (output_dir / "go.mod").write_text("module example.com/generated\n\ngo 1.23.8\n", encoding="utf-8")
    writer = generate(output_dir, bp, graph)
    writer.register(bp)
    writer.gen()

    assert _ownership_violations(output_dir) == []
