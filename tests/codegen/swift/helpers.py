from __future__ import annotations

import enum

from pathlib import Path

from api_blueprint.engine import Blueprint, ConnectionDelivery, Error, Model, Toast
from api_blueprint.engine.binary_schema import parse_binary_schema
from api_blueprint.engine.model import Array, Bool, Enum, FileField, String, Uint
from api_blueprint.writer.swift import SwiftWriter


SWIFT_CLIENT_GENERATED_HEADER = "// Code generated by api-blueprint (Swift client); DO NOT EDIT."


class StatusEnum(enum.IntEnum):
    PENDING = 1
    RUNNING = 2


class KeywordEnum(enum.Enum):
    default = "default"
    class_ = "class"


class SubmitJSON(Model):
    value = String(description="value")
    enabled = Bool(description="enabled")
    status = Enum[StatusEnum](description="status")
    keyword = Enum[KeywordEnum](description="keyword", omitempty=True)


class SubmitResponse(Model):
    status = String(description="status")
    ids = Array[Uint](description="ids")


class FormPayload(Model):
    name = String(description="name")


class MediaUpload(Model):
    title = String(description="title")
    image = FileField(content_types=["image/jpeg"], description="image")


class OpenPayload(Model):
    run_id = String(alias="run_id", description="run id")


class AssistantInput(Model):
    text = String(description="text")


class AssistantCancel(Model):
    reason = String(description="reason", omitempty=True)


class AssistantDelta(Model):
    chunk = String(description="chunk")


class CloseInfo(Model):
    reason = String(description="reason")


def demo_packet_schema():
    return parse_binary_schema(
        """
# packet DemoPacket

endian: little
content-type: application/octet-stream

## body

| field | type | count | rule | comment |
|---|---|---:|---|---|
| payload | bytes | 4 | | payload |
""".strip(),
        source_path="demo_packet.md",
    )


def write_demo_swift_package(
    out_dir: Path,
    *,
    runtime_profile: str = "modern",
    base_url: str = "http://localhost:2333",
    module: str | None = "ABClient",
    include_alt: bool = False,
) -> SwiftWriter:
    class CommonErr(Model):
        UNKNOWN = Error(
            40000,
            "common unknown",
            toast=Toast(key="common.unknown", default="Common unknown", level="error"),
        )

    class DemoErr(Model):
        UNKNOWN = Error(40001, "demo unknown")

    schema = demo_packet_schema()
    bp = Blueprint(root="/api", errors=[CommonErr])
    with bp.group("/demo") as views:
        views.GET("/ping").ARGS(q=String(description="q")).RSP(SubmitResponse)
        views.POST("/submit").REQ(SubmitJSON).RSP(SubmitResponse).ERR(DemoErr.UNKNOWN)
        views.POST("/form").REQ_FORM(FormPayload).RSP(SubmitResponse)
        views.POST("/preview").REQ_MULTIPART(MediaUpload).RSP_BYTES(content_type="image/jpeg")
        views.GET("/download").RSP_FILE(content_type="application/octet-stream", filename="sample.txt")
        views.GET("/mjpeg").RSP_BYTE_STREAM(content_type="multipart/x-mixed-replace")
        views.POST("/packet").REQ_BINARY(schema).RSP(ok=String(description="ok"))
        views.GET("/packet").RSP_BINARY_SCHEMA(schema)
        views.STREAM("/events").OPEN(OpenPayload).SERVER_MESSAGE(
            "AssistantServerMessage",
            delta=AssistantDelta,
        ).CLOSE(CloseInfo)
        views.CHANNEL("/assistant", delivery=ConnectionDelivery.UNORDERED).OPEN(OpenPayload).CLIENT_MESSAGE(
            "AssistantClientMessage",
            input=AssistantInput,
            cancel=AssistantCancel,
        ).SERVER_MESSAGE(
            "AssistantServerMessage",
            delta=AssistantDelta,
        ).CLOSE(CloseInfo)

    blueprints = [bp]
    if include_alt:
        alt = Blueprint(root="/alt")
        with alt.group("/demo") as views:
            views.GET("/ping").RSP(SubmitResponse)
        blueprints.append(alt)

    writer = SwiftWriter(
        out_dir,
        package="ApiBlueprintExampleClient",
        module=module,
        base_url=base_url,
        runtime_profile=runtime_profile,
    )
    writer.register(*blueprints)
    writer.gen()
    return writer


