def _validate_kotlin_sources(kotlin_dir: Path) -> None:
    client_dir = kotlin_dir / "client"
    server_dir = kotlin_dir / "server"
    _validate_kotlin_client_sources(client_dir)
    _validate_kotlin_server_sources(server_dir)
